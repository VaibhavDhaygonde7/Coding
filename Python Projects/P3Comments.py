# comments are the code which are ignored by the compiler
# we can write the comment by adding # before the sentence
# this is a single line comment

# we can write multi-line comment using """"""

"""
This is a 
Multi-line comment

"""

print("This is a python program")
print("This is the second line")

# the above print statement will write the second print statement in next line

print("This is a python program", end = " ")
print("This is the second line")
# end will join the next print statement to it and it will put space in between

# NOTE: we can also write end = "Vaibhav" or anything

# we can also write two statements in one print statement 
print("This is a python program", "This is the second line")
# this will automatically put a space in between the two print statements above
